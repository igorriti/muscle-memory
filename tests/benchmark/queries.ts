export const PATTERN_NAMES = Object.keys(QUERY_PATTERNS);

export function makeQuery(pattern: string, index: number): string {
  const templates = QUERY_PATTERNS[pattern];
  const template = templates[index % templates.length];
  return template.replace('{id}', String(1000 + index));
}
